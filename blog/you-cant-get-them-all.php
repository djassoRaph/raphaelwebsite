        <p class="callout">Being productive on too many “frontlines” can make you immobile, especially when others expect instant results.</p>

        <p>Right now, my life is stretched between training for my first Dan in Aikido, juggling side projects, investing in my relationship, staying on top of my day job, and just keeping my old car alive long enough to replace it. That’s already a full schedule before unexpected things come along.</p>

        <p>I recently made a deal with a colleague on a side project. I wanted to contribute, but my pace didn’t match his. He needed an “instant” app, desktop-first. I pushed for mobile-first. We ended up clashing over expectations. The contradiction wasn’t technical it was about priorities.</p>

        <p>The truth is: expertise doesn’t equal availability. I can bring value and skill, but I can’t bend time. Some people want results right away. Others understand the slower process. That mismatch often decides whether collaborations survive.</p>

        <p>So here’s the lesson I’m learning: I can’t make everyone happy. I want to become who I choose to become, and that takes process, discipline, and focus. Sometimes that means letting opportunities go if they don’t align with my priorities.</p>

        <h2>What I’m reminding myself of</h2>
        <ul>
          <li>Favor small, iterative wins over big, instant results.</li>
          <li>Say “no” without guilt. It’s how you protect the promises that matter.</li>
        </ul>
    
        <p>Training for your first Dan teaches you something crucial: progress happens through consistent, focused effort over time. You can't rush a black belt by training 16 hours one day and skipping the rest of the week.</p>

        <p>This connects to something documented in <cite><a href="http://www.catb.org/jargon/html/physical.php" target="_blank" rel="noopener">The Jargon File, version 4.4.7</a></cite> about martial arts in hacker culture. As the File notes: "Many observers have noted it, and the connection has grown noticeably stronger over time. In the 1970s, many hackers admired martial arts disciplines from a distance, sensing a compatible ideal in their exaltation of skill through rigorous self-discipline and concentration."</p>

        <p>The parallel runs deeper than just physical training. "Today's hackers readily analogize assimilation into the hacker culture with the plot of a Jet Li movie: the aspiring newbie studies with masters of the tradition, develops his art through deep meditation, ventures forth to perform heroic feats of hacking, and eventually becomes a master who trains the next generation of newbies in the hacker way."</p>

        <p>The same principle applies to professional development. You can't build a sustainable career by saying yes to every urgent request and burning out every few months. Real mastery whether in martial arts or software development requires the discipline to say no to shortcuts.</p>
        <p>Will my colleague return? I hope so. If not, it’s not failure  it’s clarity. My schedule is packed, but it’s also pointed toward the person I want to be. And that matters more than saying yes to everything.</p>

        <p><em>You can’t get them all. But you can get the right ones.</em></p>

